        "def run_market_regime_analysis():\n",
        "    \"\"\"\n",
        "    Partitions test data into regimes and evaluates classical vs. quantum models.\n",
        "    \"\"\"\n",
        "    sp_df = feat_data.get(\"SP500\", list(feat_data.values())[0])\n",
